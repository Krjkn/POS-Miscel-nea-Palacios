    private void tbUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbUsuariosMouseClicked
        int fila = tbUsuarios.getSelectedRow();
        if (fila >= 0) {
            txtidusuario.setText(tbUsuarios.getValueAt(fila, 0).toString());
            txtnomusuario.setText(tbUsuarios.getValueAt(fila, 1).toString());
            txtemail.setText(tbUsuarios.getValueAt(fila, 2).toString());
            txtpassword.setText(tbUsuarios.getValueAt(fila, 3).toString());
            txtresp1.setText(tbUsuarios.getValueAt(fila, 4).toString());
        } else {
            JOptionPane.showMessageDialog(null, "No seleccionó fila");
        }
    }//GEN-LAST:event_tbUsuariosMouseClicked

    private void tbUsuariosMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbUsuariosMouseEntered
        // Obtener la fila en la que está el mouse
        int row = tbUsuarios.rowAtPoint(evt.getPoint());
        // Seleccionar la fila si no está seleccionada
        if (!tbUsuarios.isRowSelected(row)) {
            tbUsuarios.changeSelection(row, 0, false, false);
        }
    }//GEN-LAST:event_tbUsuariosMouseEntered
    void mostrarDatos() {
        try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/miscelanea_palacios", "root", "");
        Statement stmt = conn.createStatement();
        String sql = "SELECT * FROM usuarios";
        ResultSet rs = stmt.executeQuery(sql);
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID_Usuario");
        model.addColumn("Nombre");
        model.addColumn("Correo Electroico");
        model.addColumn("Password");
        model.addColumn("Respuesta de seguridad");

        
        while (rs.next()) {
            int id = rs.getInt("ID_Usuario");
            String nombre = rs.getString("Nombre");
            String correo = rs.getString("correoElectronico");
            String contraseña = rs.getString("Password");
            String resp1 = rs.getString("respuesta1");

            model.addRow(new Object[] { id, nombre,correo, contraseña, resp1});
        }
        
        tbUsuarios.setModel(model);
        
        rs.close();
        stmt.close();
        conn.close();
    } catch (SQLException ex) {
        ex.printStackTrace();
    }

    }
    
