    private void LogInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogInButtonActionPerformed
 int ID_Usuario = Integer.parseInt(txtidusuario.getText());
        if (txtidusuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecciona un usuario");
        } else {
            if ((txtnomusuario.getText().isEmpty() || txtemail.getText().isEmpty() || txtpassword.getText().isEmpty() || txtresp1.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Llena todos los campos");
            } else {
                try{
                    ConexionDB.actualizarUsuario(txtnomusuario.getText(),txtemail.getText(), txtpassword.getText(), txtresp1.getText(),ID_Usuario);

                    txtidusuario.setText("");
                    txtnomusuario.setText("");
                    txtemail.setText("");
                    txtpassword.setText("");
                    txtresp1.setText("");
                    mostrarDatos();
                } catch(Exception e){

                    JOptionPane.showMessageDialog(null, "Error en el registro"+e);

                }
            }
        }        

    }//GEN-LAST:event_LogInButtonActionPerformed

